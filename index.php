                            .then(response => {
                                console.log(response.data);
                                alert("Data submitted successfully!");
                                // Clear form fields if needed
                                document.getElementById("contactForm").reset();
                            })
                            .catch(error => {
                                console.error('Error!', error);
                                alert("Failed to submit data. Please try again later.");
                            });
                    }
                })
                .catch(error => {
                    console.error('Error checking for duplicates:', error);
                    alert("Failed to check for duplicates. Please try again later.");
                });
        });

        // Function to generate a numerical ID starting from 0001
        function generateID() {
            const Id = Math.floor(Math.random() * 9000) + 1; // Generates a number between 1 and 9000
            return String(Id).padStart(4, '0'); // Pads the number with leading zeros to ensure 4 digits
        }

        // Function to get current date in IST
        function getCurrentISTDate() {
            const currentDate = new Date();
            return currentDate.toLocaleString('en-IN', { timeZone: 'Asia/Kolkata' });
        }

        // Function to check for duplicate email
        function isDuplicateEmail(email) {
            // Make an API call to SheetDB to check if the email exists
            // return axios.get(`https://sheetdb.io/api/v1/3ufutxy0vl8m8/search?email=${email}`)
            return axios.get(`https://sheetdb.io/api/v1/hu390hqhskoqu/search?email=${email}`)
                .then(response => {
                    // If any record with the provided email exists, return true; otherwise, return false
                    return response.data.length > 0;
                });
        }
